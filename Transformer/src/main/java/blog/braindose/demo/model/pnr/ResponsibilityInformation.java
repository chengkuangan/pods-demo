package blog.braindose.demo.model.pnr;

public class ResponsibilityInformation {
    
    private String typeOfPnrElement;
    private String agentId;
    private String responsibleOfficeID;
    
    public String getTypeOfPnrElement() {
        return typeOfPnrElement;
    }
    public void setTypeOfPnrElement(String typeOfPnrElement) {
        this.typeOfPnrElement = typeOfPnrElement;
    }
    public String getAgentId() {
        return agentId;
    }
    public void setAgentId(String agentId) {
        this.agentId = agentId;
    }
    public String getResponsibleOfficeID() {
        return responsibleOfficeID;
    }
    public void setResponsibleOfficeID(String responsibleOfficeID) {
        this.responsibleOfficeID = responsibleOfficeID;
    }

    

}
