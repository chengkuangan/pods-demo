package blog.braindose.demo.model.pnr;

public class InventoryData {
    private String posInvClassification;
    private String posInvCompanyID;
    private String posInvCountryCode;
    private String cabinCode;
    private String subClassNum;
    public String getPosInvClassification() {
        return posInvClassification;
    }
    public void setPosInvClassification(String posInvClassification) {
        this.posInvClassification = posInvClassification;
    }
    public String getPosInvCompanyID() {
        return posInvCompanyID;
    }
    public void setPosInvCompanyID(String posInvCompanyID) {
        this.posInvCompanyID = posInvCompanyID;
    }
    public String getPosInvCountryCode() {
        return posInvCountryCode;
    }
    public void setPosInvCountryCode(String posInvCountryCode) {
        this.posInvCountryCode = posInvCountryCode;
    }
    public String getCabinCode() {
        return cabinCode;
    }
    public void setCabinCode(String cabinCode) {
        this.cabinCode = cabinCode;
    }
    public String getSubClassNum() {
        return subClassNum;
    }
    public void setSubClassNum(String subClassNum) {
        this.subClassNum = subClassNum;
    }

    
}
